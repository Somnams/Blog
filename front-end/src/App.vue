<template>
  <div id="app" class="page">
    <transition name="fade">
      <keep-alive include="Blog, Life, Mine">
        <router-view v-if="isRouterAlive" />
      </keep-alive>
    </transition>
  </div>
</template>

<script>
export default {
  name: "app",
  provide() {
    return {
      reload: this.reload
    };
  },
  data() {
    return {
      isRouterAlive: true
    };
  },
  methods: {
    reload() {
      this.isRouterAlive = false;
      this.$nextTick(function() {
        this.isRouterAlive = true;
      });
    }
  }
};
</script>

